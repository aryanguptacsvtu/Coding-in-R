# Write an R script that creates a string containing a newline, tab, and a backslash using escape sequences. 
# Print the string to the console using cat(). 



str <- "This is a newline:\nThis is a tab:\tThis is a backslash: \\"

# Print the string using cat()
cat(str)
# Output :-
# This is a newline:
# This is a tab:	This is a backslash: \