# QUESTION :-
# Write R code that concatenates the strings "Data", "Science", and "R" into a single string with hyphens (-) as separators. 


# Concatenate the strings with hyphens as separators
my_string <- paste("Data", "Science", "R", sep = "-")

# Print the concatenated string
print(my_string)
# Output :-
# Data-Science-R
