# Operators in R :-

a <- 7.5
b <- 2
print(a + b)      # "Arithmetic Operators"
print(a - b)
print(a * b)
print(a / b)      # Division
print(a %% b)     # Remainder
print(a %/% b)    # Quotient
print(a ^ b)      # Power of


c1 <- c(8,9,6)    # Vectors
