# Switch Statement in R :-

x <- switch(2,
            "Ram",
            "Shyam",
            "Arun",
            "Amit",
            "Rahul")   # First Way

print(x)


a <- "80"
b <- switch(a,
            "10"="Ram",
            "23"="Shyam",
            "20"="Arun",
            "80"="Amit",
            "5"="Rahul")  # Second Way

print(b)
